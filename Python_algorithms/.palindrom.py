"""Этот код проверяет, является ли введенное пользователем число палиндромом.

Палиндром — это число, которое читается одинаково как слева направо, так и
справа налево.
"""

n_number = int(input("Введите число: "))
temp = n_number
rev = 0  # переменная для хранения числа в обратном порядке

# Используем цикл while для перебора цифр числа
while n_number > 0:
    dig = n_number % 10  # получаем последнюю цифру числа
    rev = rev * 10 + dig  # добавляем цифру к числу в обратном порядке
    n_number = n_number // 10  # убираем последнюю цифру из оригинального числа

# После завершения цикла сравниваем исходное число с его обратным порядком
if temp == rev:
    print(f"Число {temp} — палиндром!")
else:
    print(f"Число {temp} не является палиндромом!")
