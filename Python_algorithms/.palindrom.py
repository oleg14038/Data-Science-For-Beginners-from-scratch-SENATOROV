def is_palindrome(number: int) -> bool:
    """Check if the given integer is a palindrome.

    The function takes an integer `number`
    as input and returns a boolean value.
    """
    return str(number) == "".join(reversed(str(number)))


# str(n) == ".join(reversed(str(n))): Это сравнение проверяет, равно ли
# исходное строковое представление n перевернутой строке, полученной
# из reversed(str(n)).

# str(n): Преобразует число n в строку для облегчения дальнейших операций.
# reversed(str(n)): Эта функция используется для изменения символов в
# строке, полученной из str(n).
# Он возвращает итератор, который выдает символы в обратном порядке.
# Метод join в Python отвечает за объединение списка строк с помощью
# определенного указателя.


# Получите число от пользователя
input_number = int(input("Введите число:"))

# Проверьте, является ли число палиндромом
if is_palindrome(input_number):
    print("Число палиндром!")
else:
    print("Число не является палиндромом.")
